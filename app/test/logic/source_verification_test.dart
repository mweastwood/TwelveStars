          'ơ\u0309': 'ở',
          'ơ\u0303': 'ỡ',
          'ơ\u0323': 'ợ',
          'u\u0300': 'ù',
          'u\u0301': 'ú',
          'u\u0309': 'ủ',
          'u\u0303': 'ũ',
          'u\u0323': 'ụ',
          'ư\u0300': 'ừ',
          'ư\u0301': 'ứ',
          'ư\u0309': 'ử',
          'ư\u0303': 'ữ',
          'ư\u0323': 'ự',
          'y\u0300': 'ỳ',
          'y\u0301': 'ý',
          'y\u0309': 'ỷ',
          'y\u0303': 'ỹ',
          'y\u0323': 'ỵ',
        };
        for (final entry in toneMap.entries) {
          res = res.replaceAll(entry.key, entry.value);
        }

        // Normalize old-style diphthong accent placement to modern-style
        const diphthongMap = {
          'oá': 'óa',
          'oà': 'òa',
          'oả': 'ỏa',
          'oã': 'õa',
          'oạ': 'ọa',
          'uý': 'úy',
          'uỳ': 'ùy',
          'uỷ': 'ủy',
          'uỹ': 'ũy',
          'uỵ': 'ụy',
          'oé': 'óe',
          'oè': 'òe',
          'oẻ': 'ỏe',
          'oẽ': 'õe',
          'oẹ': 'ọe',
        };
        for (final entry in diphthongMap.entries) {
          res = res.replaceAll(entry.key, entry.value);
        }
      }

      final isLatin = language == PrayerLanguage.latin;
      if (isLatin) {
        // Latin is normalized (accent/diacritic stripping and ligature expansion) because:
        // 1. Liturgical Latin source texts (like the Vatican Compendium) use variable pronunciation/chanting
        //    accents (e.g. 'Dóminus', 'María') that do not alter grammatical meaning, unlike modern languages
        //    (e.g., Spanish, French, Vietnamese) where accents change word meaning.
        // 2. Different sources use ligatures (æ/œ) interchangeably with standard letter pairings (ae/oe).
        // Stripping accents/ligatures only for Latin prevents false mismatches due to style and typography
        // while preserving strict accent checks for modern languages.
        res = res
            .replaceAll('j', 'i')
            .replaceAll('æ', 'ae')
            .replaceAll('ǽ', 'ae')
            .replaceAll('œ', 'oe')
            .replaceAll('à', 'a')
            .replaceAll('á', 'a')
            .replaceAll('â', 'a')
            .replaceAll('ã', 'a')
            .replaceAll('ä', 'a')
            .replaceAll('å', 'a')
            .replaceAll('ā', 'a')
            .replaceAll('ă', 'a')
            .replaceAll('ą', 'a')
            .replaceAll('è', 'e')
            .replaceAll('é', 'e')
            .replaceAll('ê', 'e')
            .replaceAll('ë', 'e')
            .replaceAll('ē', 'e')
            .replaceAll('ĕ', 'e')
            .replaceAll('ė', 'e')
            .replaceAll('ę', 'e')
            .replaceAll('ě', 'e')
            .replaceAll('ì', 'i')
            .replaceAll('í', 'i')
            .replaceAll('î', 'i')
            .replaceAll('ï', 'i')
            .replaceAll('ĩ', 'i')
            .replaceAll('ī', 'i')
            .replaceAll('ĭ', 'i')
            .replaceAll('į', 'i')
            .replaceAll('ı', 'i')
            .replaceAll('ò', 'o')
            .replaceAll('ó', 'o')
            .replaceAll('ô', 'o')
            .replaceAll('õ', 'o')
            .replaceAll('ö', 'o')
            .replaceAll('ø', 'o')
            .replaceAll('ō', 'o')
            .replaceAll('ŏ', 'o')
            .replaceAll('ő', 'o')
            .replaceAll('ù', 'u')
            .replaceAll('ú', 'u')
            .replaceAll('û', 'u')
            .replaceAll('ü', 'u')
            .replaceAll('ũ', 'u')
            .replaceAll('ū', 'u')
            .replaceAll('ŭ', 'u')
            .replaceAll('ů', 'u')
            .replaceAll('ű', 'u')
            .replaceAll('ų', 'u')
            .replaceAll('ç', 'c')
            .replaceAll('ć', 'c')
            .replaceAll('ĉ', 'c')
            .replaceAll('ċ', 'c')
            .replaceAll('č', 'c')
            .replaceAll('ď', 'd')
            .replaceAll('đ', 'd')
            .replaceAll('ĝ', 'g')
            .replaceAll('ğ', 'g')
            .replaceAll('ġ', 'g')
            .replaceAll('ģ', 'g')
            .replaceAll('ĥ', 'h')
            .replaceAll('ħ', 'h')
            .replaceAll('ĵ', 'j')
            .replaceAll('ķ', 'k')
            .replaceAll('ĺ', 'l')
            .replaceAll('ļ', 'l')
            .replaceAll('ľ', 'l')
            .replaceAll('ł', 'l')
            .replaceAll('ń', 'n')
            .replaceAll('ņ', 'n')
            .replaceAll('ň', 'n')
            .replaceAll('ŉ', 'n')
            .replaceAll('ŕ', 'r')
            .replaceAll('ŗ', 'r')
            .replaceAll('ř', 'r')
            .replaceAll('ś', 's')
            .replaceAll('ŝ', 's')
            .replaceAll('ş', 's')
            .replaceAll('š', 's')
            .replaceAll('ţ', 't')
            .replaceAll('ť', 't')
            .replaceAll('ŧ', 't')
            .replaceAll('ŵ', 'w')
            .replaceAll('ŷ', 'y')
            .replaceAll('ÿ', 'y')
            .replaceAll('ź', 'z')
            .replaceAll('ż', 'z')
            .replaceAll('ž', 'z');
      }

      res = res
          .replaceAll('’', "'")
          .replaceAll('‘', "'")
          .replaceAll('“', '"')
          .replaceAll('”', '"');

      // Strip all straight quotes first
      res = res.replaceAll('"', '').replaceAll("'", '');

      // Strip all punctuation and whitespace to do a character-sequence only match.
      // This includes Western and Chinese full-width punctuation.
      res = res.replaceAll(
        RegExp(r"[.,;:!?\-\(\)«»‘’“”\s\u00A0\u200b，。、；：！？「」『』/]+"),
        '',
      );

      res = res
          .replaceAll('amen', '')
          .replaceAll('amén', '')
          .replaceAll('amên', '')
          .replaceAll('亞孟', '')
          .replaceAll('阿們', '')
          .replaceAll('阿門', '')
          .replaceAll('priest', '')
          .replaceAll('people', '')
          .replaceAll('deacon', '')
          .replaceAll('sacerdos', '')
          .replaceAll('populus', '')
          .replaceAll('diaconus', '')
          .replaceAll('sacerdote', '')
          .replaceAll('asamblea', '')
          .replaceAll('diácono', '')
          .replaceAll('✠', '')
          .replaceAll('†', '')
          .replaceAll('&#10016;', '')
          .replaceAll('&#8224;', '');

      return res;
    }

    // Open compiled JSON database
    final jsonFile = File('assets/prayers.json');
    if (!jsonFile.existsSync()) {
      throw Exception(
        'assets/prayers.json does not exist. Run bin/assemble_db.dart first.',
      );
    }

    final List<dynamic> prayersList =
        jsonDecode(jsonFile.readAsStringSync()) as List<dynamic>;

    // Run remote URL verification checks for each translation
    for (final pMap in prayersList) {
      final prayerId = pMap['id'] as String;
      final transMap = pMap['translations'] as Map<String, dynamic>;

      for (final entry in transMap.entries) {
        final languageStr = entry.key;
        final language = PrayerLanguage.values.firstWhere(
          (e) => e.toString().split('.').last == languageStr,
          orElse: () => PrayerLanguage.english,
        );

        final transList = entry.value as List<dynamic>;
        final versionCount = transList.length;

        for (
          int versionIndex = 0;
          versionIndex < versionCount;
          versionIndex++
        ) {
          final tMap = transList[versionIndex] as Map<String, dynamic>;
          final text = tMap['text'] as String;
          final sourceUrl = tMap['source_url'] as String;
          final suffix = versionCount > 1
              ? ' (Version ${versionIndex + 1})'
              : '';

          test(
            'Verify $prayerId in ${language.name}$suffix matches source text',
            () async {
              try {
                final sourcesList = tMap['sources'] as List<dynamic>?;

                // Split the prayer into lines by newline to verify each line exists on the page.
                final lines = text
                    .split('\n')
                    .map((line) => softNormalize(line, language: language))
                    .where((line) => line.isNotEmpty)
                    .toList();

                bool allLinesMatched = true;
                final missingLines = <String>[];
                final missingDetails = <String>[];

                if (sourcesList != null && sourcesList.isNotEmpty) {
                  // Multi-source verification
                  for (final src in sourcesList) {
                    final srcMap = src as Map<String, dynamic>;
                    final srcName = srcMap['name'] as String;
                    final srcUrl = srcMap['url'] as String;
                    final startLine = srcMap['start_line'] as int;
                    final endLine = srcMap['end_line'] as int;

                    final html = await fetchHtml(srcUrl);
                    final document = html_parser.parse(html);
                    final pageText = document.body?.text ?? '';
                    final softPage = softNormalize(
                      pageText,
                      language: language,
                    );

                    // Extract the lines belonging to this source (1-based indices)
                    final srcLines = lines.sublist(startLine - 1, endLine);
                    for (final line in srcLines) {
                      if (!softPage.contains(line)) {
                        allLinesMatched = false;
                        missingLines.add(line);
                        missingDetails.add('"$line" (from $srcName: $srcUrl)');
                      }
                    }
                  }
                } else {
                  // Fallback to standard single-source verification
                  final html = await fetchHtml(sourceUrl);
                  final document = html_parser.parse(html);
                  final pageText = document.body?.text ?? '';
                  final softPage = softNormalize(pageText, language: language);

                  for (final line in lines) {
                    if (!softPage.contains(line)) {
                      allLinesMatched = false;
                      missingLines.add(line);
                      missingDetails.add('"$line" (from $sourceUrl)');
                    }
                  }
                }

                if (!allLinesMatched) {
                  final errorMsg =
                      'Prayer text was not found in the source URLs:\n'
                      'Missing lines:\n${missingDetails.join('\n')}';
                  fail(errorMsg);
                }

                expect(allLinesMatched, isTrue);
              } on NetworkSkipException catch (e) {
                // ignore: avoid_print
                print('Skipping test due to network/blocking issue: $e');
              }
            },
            timeout: const Timeout(Duration(seconds: 30)),
          );
        }
      }
    }
  });
